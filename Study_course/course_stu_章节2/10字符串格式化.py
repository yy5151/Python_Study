# 占位拼接 %s
# % :占个位置
# s :将变量变成字符串放入占位的地方
name = "黑马程序员"
message = "学IT就来%s" % name
print(message)

g_age = 22
b_age = 26
# 多个变量占位,变量用括号括起来,并按占位顺序填入
print("我的年龄是%s岁,他的年龄是%s岁" % (g_age,b_age))

"""
 %s将内容转换成字符串,放入占位位置
 %d将内容转换为整数,放入占位位置
 %f将内容转换成浮点型,放入占位位置
"""
me = "元盈"
myage = 22
tall = 153.3
print("我是%s,我的年龄是%s,我的身高是%s厘米"%(me,myage,tall))

# f"内容{变量}"快速格式化
# 不限变量类型,不限浮点数精度控制
print(f"我是{me},年龄{myage},身高{tall}")
