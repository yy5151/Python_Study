"""
字典的操作
"""

s_list = {"益海":{"语文":78,"数学":89,"英语":92},
          "桃桃":{"语文":98,"数学":89,"英语":97},
          "艺洋":{"语文":83,"数学":99,"英语":72}}

# 新增元素
# 语法：字典[key] = value
# 结果：字典被修改，新增了元素
# 当 key 是不存在就是新增元素
d = {"语文":78,"数学":89,"英语":92}
s_list["小爱"] = d
print(s_list)


print()
# 更新元素
# 语法:字典[key] = value
# 结果：字典被修改，元素被更新
# 当 key 已经存在就是更新操作
new_d = {"语文":78,"数学":89,"python":92}
s_list["小爱"] = new_d
print(s_list)






